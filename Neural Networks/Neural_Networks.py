import numpy as np
import tensorflow as tf
from tensorflow import keras



#Converting decimal to binary returns binary

def dec_bin(Num):
  binary=[]
  i=0
  while Num>0:
      i+=1
      temp=Num%2**i
      Num-=temp
      if temp>0:
          temp=1
      binary.append(temp)
  return binary

#data set 
X=np.zeros((2**10,10),dtype=int)
Y=np.zeros(2**10,dtype=int)



for i in range(2**10):
  binnum=dec_bin(i) # convert to binary
  for j in range(len(binnum)):
      X[i][j]=binnum[j]
for i in range(2**10):
  Y[i]=(sum(X[i])%4==0) #check number of 1 %4
X=np.asanyarray(X)
Y=np.asanyarray(Y)
Y=Y.reshape((1024,1))
DATA=np.hstack((X,Y))
np.random.shuffle(DATA)

N=10

X_train=DATA[:int(1024*0.8),:10]
Y_train=DATA[:int(1024*0.8),-1]

X_test=DATA[int(1024*0.8):,:10]
Y_test=DATA[int(1024*0.8):,-1]
 

X_train.shape
X_test.shape


model = keras.Sequential([keras.layers.Dense(5, activation=tf.nn.relu),
                          keras.layers.Dense(3, activation=tf.nn.relu),
                          keras.layers.Dense(2, activation=tf.nn.relu)])

model.compile(optimizer=tf.train.AdamOptimizer(), 
              loss='sparse_categorical_crossentropy',
              metrics=['accuracy'])

model.fit(X_train, Y_train, epochs=500)

test_loss, test_acc = model.evaluate(X_test, Y_test)

print('Test accuracy:', test_acc)


